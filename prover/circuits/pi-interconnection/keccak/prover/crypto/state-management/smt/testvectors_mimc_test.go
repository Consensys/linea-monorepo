// Root hash of the empty tree
func TestEmptyRootHashMiMC(t *testing.T) {
	config := &smt.Config{
		HashFunc: hashtypes.MiMC,
		Depth:    40,
	}

	tree := smt.NewEmptyTree(config)

	// Root hash of the empty tree
	require.Equal(
		t,
		"0x09c43afd684b1243cc64af08b18565a71a7f504ecf9721f3c1cd3704f71bdff6",
		tree.Root.Hex(),
	)
}

