package main

import (
	"bytes"
	"fmt"
	"go/ast"
	"go/format"
	"go/parser"
	"go/token"
	"log"
	"os"
	"path/filepath"
	"sort"
	"strings"
	"text/template"
)

var (
	pkgName    = "serde"
	inputFile  = "impl_registry.go"
	outputFile = "registry.go"
	regFnName  = "RegisterImplementation"
	sourcePath = filepath.Join("..", "..", "protocol", pkgName, inputFile)
	outputPath = filepath.Join("..", "..", "protocol", pkgName, outputFile)
)

const (
	fileTemplate = `// Code generated by codegen. DO NOT EDIT.
package {{.PkgName}}

import (
	"reflect"
{{range .Imports}}	{{if .Alias}}{{.Alias}} {{end}}"{{.Path}}"
{{end}})

// IDToType maps a uint16 ID to the concrete reflect.Type.
// This is O(1) access for Deserialization.
var IDToType = [...]reflect.Type{
{{range .Entries}}	reflect.TypeOf({{.Expr}}), // ID: {{.ID}}
{{end}}}

// TypeToID maps a reflect.Type to a uint16 ID.
// This is O(1) access for Serialization.
var TypeToID = map[reflect.Type]uint16{
{{range .Entries}}	reflect.TypeOf({{.Expr}}): {{.ID}},
{{end}}}
`
)

type entry struct {
	ID   int
	Expr string
}

type importDef struct {
	Alias string
	Path  string
}

func main() {
	fset := token.NewFileSet()
	node, err := parser.ParseFile(fset, sourcePath, nil, parser.ParseComments)
	if err != nil {
		log.Fatalf("failed to parse source registry file: %v", err)
	}

	if pkgName == "" {
		log.Fatal("pkgName must not be empty")
	}

	// 1. Read source content once for offset mapping
	sourceContent, err := os.ReadFile(sourcePath)
	if err != nil {
		log.Fatalf("failed to read source registry file: %v", err)
	}

	// 2. Extract RegisterImplementation arguments first
	// This must happen before import filtering so we know what is "used"
	var types []string
	ast.Inspect(node, func(n ast.Node) bool {
		call, ok := n.(*ast.CallExpr)
		if !ok {
			return true
		}

		var funcName string
		switch fun := call.Fun.(type) {
		case *ast.Ident:
			funcName = fun.Name
		case *ast.SelectorExpr:
			funcName = fun.Sel.Name
		}

		if funcName == regFnName && len(call.Args) > 0 {
			start := fset.Position(call.Args[0].Pos()).Offset
			end := fset.Position(call.Args[0].End()).Offset
			types = append(types, string(sourceContent[start:end]))
		}
		return true
	})

	//  Deduplicate the types slice
	uniqueTypes := make(map[string]bool)
	dedupedTypes := make([]string, 0, len(types))
	for _, t := range types {
		if !uniqueTypes[t] {
			uniqueTypes[t] = true
			dedupedTypes = append(dedupedTypes, t)
		}
	}
	types = dedupedTypes

	// Deterministic ordering for ID assignment
	sort.Strings(types)

	// 3. Extract and filter imports based on 'types'
	// Pre-allocate with 0 length and node.Imports capacity to avoid leading empty elements
	imports := make([]importDef, 0, len(node.Imports))
	for _, imp := range node.Imports {
		path := strings.Trim(imp.Path.Value, "\"")

		// 'reflect' is hardcoded in the template; skip to avoid duplicates
		if path == "reflect" {
			continue
		}

		isUsed := false
		// Determine the package identifier used in code (either the alias or the last path segment)
		pkgIdentifier := path[strings.LastIndex(path, "/")+1:]
		if imp.Name != nil {
			pkgIdentifier = imp.Name.Name
		}

		// Check if any registered type uses this package identifier as a selector
		for _, t := range types {
			if strings.Contains(t, pkgIdentifier+".") {
				isUsed = true
				break
			}
		}

		if isUsed {
			var alias string
			if imp.Name != nil {
				alias = imp.Name.Name
			}
			imports = append(imports, importDef{
				Alias: alias,
				Path:  path,
			})
		}
	}

	// 4. Prepare template data
	entries := make([]entry, 0, len(types))
	for i, t := range types {
		entries = append(entries, entry{
			ID:   i,
			Expr: t,
		})
	}

	data := struct {
		PkgName string
		Imports []importDef
		Entries []entry
	}{
		PkgName: pkgName,
		Imports: imports,
		Entries: entries,
	}

	// 5. Generate code
	tmpl := template.Must(template.New("gen").Parse(fileTemplate))
	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		log.Fatalf("template execution failed: %v", err)
	}

	// Format the source (runs gofmt logic)
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		fmt.Println(buf.String())
		log.Fatalf("gofmt failed: %v", err)
	}

	// 6. Skip write if content is unchanged to avoid unnecessary CI triggers
	if existing, err := os.ReadFile(outputPath); err == nil {
		if bytes.Equal(existing, formatted) {
			fmt.Printf("No changes to %s (%d types).\n", outputPath, len(entries))
			return
		}
	}

	// 7. Atomic write using 0600 permissions to satisfy gosec
	tmpPath := outputPath + ".tmp"
	if err := os.WriteFile(tmpPath, formatted, 0600); err != nil {
		log.Fatalf("failed to write temp file: %v", err)
	}

	if err := os.Rename(tmpPath, outputPath); err != nil {
		log.Fatalf("failed to replace output file: %v", err)
	}

	fmt.Printf("Successfully generated %s with %d types.\n", outputPath, len(entries))
}
