func init() {
	_, filename, _, ok := runtime.Caller(0)
	if !ok {
		panic("Could not determine location of main.go")
	}
	cmdDir := filepath.Dir(filename)
	proverDir := filepath.Dir(filepath.Dir(filepath.Dir(cmdDir)))
	outputPath = filepath.Join(proverDir, "protocol", "serialization", "serialization_gen.go")
}

