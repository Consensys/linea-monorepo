type FieldInfo struct {
	Name              string
	TypeStr           string
	IsPrimitive       bool
	IsArray           bool
	IsPointer         bool
	UnderlyingTypeStr string
	SliceTypeStr      string
	Special           *SpecialHandler
	DirectCall        string
	Accessor          string
}

type StructInfo struct {
	FuncName      string
	QualifiedName string
	NumFields     int
	Fields        []FieldInfo
}

type ImportInfo struct {
	Path  string
	Alias string
}

var generatedFunctionNames = make(map[string]bool)
var typeToFunctionName = make(map[string]string)

var structsToGenerate []StructInfo
var requiredImports = make(map[string]string)

func main() {
	if len(os.Args) < 2 {
		log.Fatal("Usage: go run main.go <target-package-path>")
	}
	targetPackage := os.Args[1]

	cfg := &packages.Config{
		Mode: packages.NeedTypes | packages.NeedTypesInfo | packages.NeedSyntax | packages.NeedImports,
	}
	pkgs, err := packages.Load(cfg, targetPackage)
	if err != nil {
		log.Fatal(err)
	}

	if packages.PrintErrors(pkgs) > 0 {
		os.Exit(1)
	}
	pkg := pkgs[0]

	var queue []types.Type
	scope := pkg.Types.Scope()

	for _, name := range seedTypes {
		obj := scope.Lookup(name)
		if obj == nil {
			log.Printf("Warning: Seed type %s not found in package", name)
			continue
		}
		queue = append(queue, obj.Type())
	}

	// --- PASS 1: Discovery ---
	discoveryQueue := make([]types.Type, len(queue))
	copy(discoveryQueue, queue)

	for len(discoveryQueue) > 0 {
		t := discoveryQueue[0]
		discoveryQueue = discoveryQueue[1:]

		if ptr, ok := t.(*types.Pointer); ok {
			t = ptr.Elem()
		}
		named, ok := t.(*types.Named)
		if !ok {
			continue
		}

		typeID := types.TypeString(named, nil)

		if _, exists := typeToFunctionName[typeID]; exists {
			continue
		}

		if !strings.HasPrefix(named.Obj().Pkg().Path(), projectPackagePrefix) {
			continue
		}

		baseName := toPascalCase(named.Obj().Pkg().Name()) + toPascalCase(named.Obj().Name())
		if args := named.TypeArgs(); args != nil && args.Len() > 0 {
			baseName += "Of"
			for i := 0; i < args.Len(); i++ {
				arg := args.At(i)
				baseName += cleanTypeName(arg)
				if i < args.Len()-1 {
					baseName += "And"
				}
			}
		}

		funcName := baseName
		counter := 1
		for generatedFunctionNames[funcName] {
			funcName = fmt.Sprintf("%s%d", baseName, counter)
			counter++
		}

		generatedFunctionNames[funcName] = true
		typeToFunctionName[typeID] = funcName

		st, ok := named.Underlying().(*types.Struct)
		if !ok {
			continue
		}
		for i := 0; i < st.NumFields(); i++ {
			f := st.Field(i)
			tag := st.Tag(i)
			if strings.Contains(tag, `serde:"omit"`) || !f.Exported() {
				continue
			}
			inspectAndQueue(f.Type(), &discoveryQueue)
		}
	}

	// --- PASS 2: Generation ---
	genVisited := make(map[string]bool)

	for len(queue) > 0 {
		t := queue[0]
		queue = queue[1:]

		if ptr, ok := t.(*types.Pointer); ok {
			t = ptr.Elem()
		}
		named, ok := t.(*types.Named)
		if !ok {
			continue
		}

		typeID := types.TypeString(named, nil)
		if genVisited[typeID] {
			continue
		}

		funcName, ok := typeToFunctionName[typeID]
		if !ok {
			continue
		}

		genVisited[typeID] = true

		st, ok := named.Underlying().(*types.Struct)
		if !ok {
			continue
		}

		registerImport(named.Obj().Pkg())
		qualifiedName := types.TypeString(named, typeQualifier)

		var fields []FieldInfo
		for i := 0; i < st.NumFields(); i++ {
			f := st.Field(i)
			tag := st.Tag(i)

			if strings.Contains(tag, `serde:"omit"`) || !f.Exported() {
				continue
			}

			inspectAndQueue(f.Type(), &queue)

			typeStr := types.TypeString(f.Type(), typeQualifier)
			special := checkSpecial(f.Type())
			accessor := "v." + f.Name()
			isPrim, isArr := isPrimitiveOrArray(f.Type())

			var underlyingStr, sliceStr string
			if isPrim {
				// Resolve underlying type string (e.g. "int" or "string")
				underlyingStr = types.TypeString(getUnderlying(f.Type()), nil)
				if isArr {
					if arr, ok := getUnderlying(f.Type()).(*types.Array); ok {
						sliceStr = "[]" + types.TypeString(arr.Elem(), nil)
					}
				}
			}

			var directCall string
			var isPointer bool

			ft := f.Type()
			if ptr, ok := ft.(*types.Pointer); ok {
				ft = ptr.Elem()
				isPointer = true
			}

			if namedField, ok := ft.(*types.Named); ok {
				ftID := types.TypeString(namedField, nil)
				if fn, exists := typeToFunctionName[ftID]; exists {
					directCall = fn
				}
			}

			fields = append(fields, FieldInfo{
				Name:              f.Name(),
				TypeStr:           typeStr,
				IsPrimitive:       isPrim,
				IsArray:           isArr,
				IsPointer:         isPointer,
				UnderlyingTypeStr: underlyingStr,
				SliceTypeStr:      sliceStr,
				Special:           special,
				Accessor:          accessor,
				DirectCall:        directCall,
			})
		}

		structsToGenerate = append(structsToGenerate, StructInfo{
			FuncName:      funcName,
			QualifiedName: qualifiedName,
			NumFields:     len(fields),
			Fields:        fields,
		})
	}

	generateOutput()
}

// --- HELPERS ---

func toPascalCase(s string) string {
	if s == "" {
		return ""
	}
	runes := []rune(s)
	runes[0] = unicode.ToUpper(runes[0])
	return string(runes)
}

func cleanTypeName(t types.Type) string {
	switch u := t.(type) {
	case *types.Pointer:
		return cleanTypeName(u.Elem())
	case *types.Slice:
		return "SliceOf" + cleanTypeName(u.Elem())
	case *types.Named:
		return toPascalCase(u.Obj().Name())
	case *types.Basic:
		return toPascalCase(u.Name())
	default:
		return "Unknown"
	}
}

func registerImport(p *types.Package) {
	if p == nil || p.Path() == outputPackagePath {
		return
	}
	alias := p.Name()
	if existingPath, occupied := findPathByAlias(alias); occupied {
		if existingPath != p.Path() {
			alias = sanitizeName(p.Path())
		}
	}
	requiredImports[p.Path()] = alias
}

func findPathByAlias(alias string) (string, bool) {
	for path, a := range requiredImports {
		if a == alias {
			return path, true
		}
	}
	return "", false
}

func typeQualifier(p *types.Package) string {
	if p == nil || p.Path() == outputPackagePath {
		return ""
	}
	registerImport(p)
	return requiredImports[p.Path()]
}

func sanitizeName(s string) string {
	return strings.Map(func(r rune) rune {
		if (r >= 'a' && r <= 'z') || (r >= 'A' && r <= 'Z') || (r >= '0' && r <= '9') {
			return r
		}
		return '_'
	}, s)
}

func checkSpecial(t types.Type) *SpecialHandler {
	base := t
	if ptr, ok := t.(*types.Pointer); ok {
		base = ptr.Elem()
	}
	key := types.TypeString(base, nil)
	if h, ok := specialHandlers[key]; ok {
		return &h
	}
	return nil
}

func inspectAndQueue(t types.Type, queue *[]types.Type) {
	switch actual := t.(type) {
	case *types.Pointer:
		inspectAndQueue(actual.Elem(), queue)
	case *types.Slice:
		inspectAndQueue(actual.Elem(), queue)
	case *types.Array:
		inspectAndQueue(actual.Elem(), queue)
	case *types.Map:
		inspectAndQueue(actual.Key(), queue)
		inspectAndQueue(actual.Elem(), queue)
	case *types.Named:
		*queue = append(*queue, actual)
	}
}

func isPrimitiveOrArray(t types.Type) (bool, bool) {
	underlying := getUnderlying(t)
	switch u := underlying.(type) {
	case *types.Basic:
		return u.Kind() != types.UntypedNil && u.Kind() != types.UnsafePointer, false
	case *types.Slice:
		if elem, ok := u.Elem().(*types.Basic); ok {
			return elem.Kind() == types.Byte, false
		}
	case *types.Array:
		if elem, ok := u.Elem().(*types.Basic); ok {
			return elem.Kind() == types.Byte, true
		}
	}
	return false, false
}

func getUnderlying(t types.Type) types.Type {
	if named, ok := t.(*types.Named); ok {
		return getUnderlying(named.Underlying())
	}
	return t
}

func generateOutput() {
	var imports []ImportInfo
	for path, alias := range requiredImports {
		imports = append(imports, ImportInfo{Path: path, Alias: alias})
	}
	sort.Slice(imports, func(i, j int) bool {
		return imports[i].Path < imports[j].Path
	})

	data := struct {
		PackageName string
		Imports     []ImportInfo
		Structs     []StructInfo
	}{
		PackageName: outputPackageName,
		Imports:     imports,
		Structs:     structsToGenerate,
	}

	t := template.Must(template.New("gen").Parse(fileTemplate))
	var buf bytes.Buffer
	if err := t.Execute(&buf, data); err != nil {
		log.Fatal(err)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		fmt.Println(buf.String())
		log.Fatalf("Format error: %v", err)
	}

	if err := os.WriteFile(outputPath, formatted, 0644); err != nil {
		log.Fatal(err)
	}
	fmt.Printf("Generated fast codecs for %d types at %s\n", len(structsToGenerate), outputPath)
}
