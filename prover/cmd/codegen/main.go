package main

import (
	"bytes"
	"fmt"
	"go/ast"
	"go/format"
	"go/parser"
	"go/token"
	"log"
	"os"
	"path/filepath"
	"sort"
	"strings"
	"text/template"
)

var (
	sourcePath = filepath.Join("..", "..", "protocol", "serialization", "implementation_registry.go")
	outputPath = filepath.Join("..", "..", "protocol", "serialization", "registry.go")
)

// The path to your existing source of truth
const (
	fileTemplate = `// Code generated by codegen. DO NOT EDIT.
package serialization

import (
	"reflect"
{{range .Imports}}	{{if .Alias}}{{.Alias}} {{end}}"{{.Path}}"
{{end}})

// IDToType maps a uint16 ID to the concrete reflect.Type.
// This is O(1) access for Deserialization.
var IDToType = [...]reflect.Type{
{{range .Entries}}	reflect.TypeOf({{.Expr}}), // ID: {{.ID}}
{{end}}}

// TypeToID maps a reflect.Type to a uint16 ID.
// This is O(1) access for Serialization.
var TypeToID = map[reflect.Type]uint16{
{{range .Entries}}	reflect.TypeOf({{.Expr}}): {{.ID}},
{{end}}}
`
)

type entry struct {
	ID   int
	Expr string // The actual code string, e.g., "column.Natural{}"
}

type importDef struct {
	Alias string
	Path  string
}

func main() {
	fset := token.NewFileSet()
	node, err := parser.ParseFile(fset, sourcePath, nil, parser.ParseComments)
	if err != nil {
		log.Fatalf("Failed to parse registry file: %v", err)
	}

	// 1. Extract Imports WITH Aliases
	var imports []importDef
	for _, imp := range node.Imports {
		path := strings.Trim(imp.Path.Value, "\"")
		var alias string

		// Capture the alias (e.g., "cmimc", "ded", "_", or ".") if it exists
		if imp.Name != nil {
			alias = imp.Name.Name
		}

		imports = append(imports, importDef{Alias: alias, Path: path})
	}

	// 2. Extract Types from init() -> RegisterImplementation calls
	var types []string

	ast.Inspect(node, func(n ast.Node) bool {
		// Look for CallExpr (function calls)
		call, ok := n.(*ast.CallExpr)
		if !ok {
			return true
		}

		// Check if the function being called is "RegisterImplementation"
		// This handles both "RegisterImplementation(...)" and "pkg.RegisterImplementation(...)"
		var funcName string
		switch fun := call.Fun.(type) {
		case *ast.Ident:
			funcName = fun.Name
		case *ast.SelectorExpr:
			funcName = fun.Sel.Name
		}

		if funcName == "RegisterImplementation" {
			if len(call.Args) > 0 {
				// Get the exact source code of the argument (e.g., "ded.AssignPIPProverAction{}")
				start := fset.Position(call.Args[0].Pos()).Offset
				end := fset.Position(call.Args[0].End()).Offset

				// We read the file bytes to extract the exact string
				content, _ := os.ReadFile(sourcePath)
				exprStr := string(content[start:end])
				types = append(types, exprStr)
			}
		}
		return true
	})

	// 3. Sort Types Deterministically
	sort.Strings(types)

	// 4. Prepare Data for Template
	var entries []entry
	for i, t := range types {
		entries = append(entries, entry{ID: i, Expr: t})
	}

	// 5. Generate Output
	data := struct {
		Imports []importDef
		Entries []entry
	}{
		Imports: imports,
		Entries: entries,
	}

	t := template.Must(template.New("gen").Parse(fileTemplate))
	var buf bytes.Buffer
	if err := t.Execute(&buf, data); err != nil {
		log.Fatal(err)
	}

	// Format the code (gofmt)
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		fmt.Println(buf.String()) // Print unformatted code for debugging
		log.Fatalf("Failed to format generated code: %v", err)
	}

	if err := os.WriteFile(outputPath, formatted, 0644); err != nil {
		log.Fatal(err)
	}

	fmt.Printf("Successfully generated %s with %d types.\n", outputPath, len(entries))
}
