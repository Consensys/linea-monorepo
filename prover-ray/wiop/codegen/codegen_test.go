// TestGenerate_RoundStructure verifies that:
//   - there is exactly one AdvanceRound() call per round
//   - there is exactly one .Run(rt) call per registered ProverAction
//   - there is exactly one .Check(rt) call per registered VerifierAction
func TestGenerate_RoundStructure(t *testing.T) {
	sys, vars := compiledFibSystem(t)
	src := generate(t, sys, vars, codegen.Options{Package: "generated"})

	var wantProver, wantVerifier int
	for _, r := range sys.Rounds {
		wantProver += len(r.ProverActions)
		wantVerifier += len(r.VerifierActions)
	}

	assert.Equal(t, len(sys.Rounds), strings.Count(src, "rt.AdvanceRound()"),
		"must emit one AdvanceRound() per round")
	assert.Equal(t, wantProver, strings.Count(src, ".Run(rt)"),
		"must emit one .Run(rt) per ProverAction")
	assert.Equal(t, wantVerifier, strings.Count(src, ".Check(rt)"),
		"must emit one .Check(rt) per VerifierAction")
}

// TestGenerate_RoundComments verifies that each round has a section comment.
func TestGenerate_RoundComments(t *testing.T) {
	sys, vars := compiledFibSystem(t)
	src := generate(t, sys, vars, codegen.Options{Package: "generated"})

	for i := range sys.Rounds {
		assert.Contains(t, src, "=== Round", "round %d must have a section comment", i)
	}
}

// fibExpectedSource is the synthesised expected output for the Fibonacci
// scenario. It is written without caring about exact var-block alignment;
// normaliseSource runs go/format on it before comparison so both sides are
// canonically formatted.
//
// Structure: 3 rounds created by global.Compile on top of the one round from
// the scenario itself.
//   - Round 0 (r0): no prover actions — the column is committed here.
//   - Round 1 (quotientRound): QuotientProverAction computes quotient shares.
//   - Round 2 (evalRound): EvalProverAction evaluates columns at a random
//     point; GlobalVerifier checks the PLONK quotient identity.
const fibExpectedSource = `// Code generated by wiop/codegen on <timestamp>. DO NOT EDIT.
// Regenerate with: go generate ./wiop/codegen

package generated

import (
	"fmt"
	"github.com/consensys/linea-monorepo/prover-ray/wiop"
	"github.com/consensys/linea-monorepo/prover-ray/wiop/compilers/global"
)

// Action variables are populated by the caller's init() function.
var (
	_act1   *global.QuotientProverAction
	_act2   *global.EvalProverAction
	_check3 *global.GlobalVerifier
)

// Prove executes all prover rounds and verifier checks in declaration order.
// It is the canonical entry point for proof generation.
func Prove(rt *wiop.Runtime) error {

	// === Round 0 ===
	rt.AdvanceRound()

	// === Round 1 ===
	_act1.Run(rt)
	rt.AdvanceRound()

	// === Round 2 ===
	_act2.Run(rt)
	if err := _check3.Check(rt); err != nil {
		return fmt.Errorf("round 2 verifier: %w", err)
	}
	rt.AdvanceRound()

	return nil
}
`

// normaliseSource runs go/format and replaces the timestamp line with a fixed
// placeholder so that golden comparisons are stable across runs.
func normaliseSource(t *testing.T, src string) string {
	t.Helper()
	// Replace the timestamp before formatting so the placeholder survives.
	var b strings.Builder
	for line := range strings.SplitSeq(src, "\n") {
		if strings.HasPrefix(strings.TrimSpace(line), "// Code generated by") {
			b.WriteString("// Code generated by wiop/codegen on <timestamp>. DO NOT EDIT.\n")
		} else {
			b.WriteString(line + "\n")
		}
	}
	normalised, err := format.Source([]byte(b.String()))
	require.NoError(t, err, "normaliseSource: go/format failed on:\n%s", b.String())
	return string(normalised)
}

// TestGenerate_GoldenFibonacci compares the full generated output for the
// Fibonacci vanishing scenario against the synthesised expected source above.
// It validates that the generator produces the correct package declaration,
// imports, var block (with concrete action types), and Prove function body.
func TestGenerate_GoldenFibonacci(t *testing.T) {
	sys, vars := compiledFibSystem(t)
	actual := generate(t, sys, vars, codegen.Options{Package: "generated"})

	assert.Equal(t,
		normaliseSource(t, fibExpectedSource),
		normaliseSource(t, actual),
	)
}

// TestGenerate_Idempotent verifies that calling Generate twice on the same
// system with the same vars produces identical output (modulo the timestamp).
func TestGenerate_Idempotent(t *testing.T) {
	sys, vars := compiledFibSystem(t)
	opts := codegen.Options{Package: "generated"}

	src1 := generate(t, sys, vars, opts)
	src2 := generate(t, sys, vars, opts)

	stripTimestamp := func(s string) string {
		var b strings.Builder
		for line := range strings.SplitSeq(s, "\n") {
			if strings.HasPrefix(strings.TrimSpace(line), "// Code generated by") {
				continue
			}
			b.WriteString(line + "\n")
		}
		return b.String()
	}

	assert.Equal(t, stripTimestamp(src1), stripTimestamp(src2),
		"Generate must be deterministic across calls")
}
