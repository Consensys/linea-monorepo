EXPECTED_L1_PROXY_ADMIN="$(compute_expected_address "$L1_DEPLOYER_ADDRESS" "$EXPECTED_L1_PROXY_ADMIN_NONCE" "L1 ProxyAdmin")"
EXPECTED_L1_TOKEN_BEACON="$(compute_expected_address "$L1_DEPLOYER_ADDRESS" "$EXPECTED_L1_TOKEN_BEACON_NONCE" "L1 UpgradeableBeacon")"
EXPECTED_L1_TOKEN_BRIDGE="$(compute_expected_address "$L1_DEPLOYER_ADDRESS" "$EXPECTED_L1_TOKEN_BRIDGE_NONCE" "L1 TokenBridge")"
EXPECTED_L2_BRIDGED_TOKEN="$(compute_expected_address "$PRECOMPUTED_L2_DEPLOYER" "$EXPECTED_L2_BRIDGED_TOKEN_NONCE" "L2 BridgedToken")"
EXPECTED_L2_TOKEN_BRIDGE_IMPL="$(compute_expected_address "$PRECOMPUTED_L2_DEPLOYER" "$EXPECTED_L2_TOKEN_BRIDGE_IMPL_NONCE" "L2 TokenBridge implementation")"
EXPECTED_L2_PROXY_ADMIN="$(compute_expected_address "$PRECOMPUTED_L2_DEPLOYER" "$EXPECTED_L2_PROXY_ADMIN_NONCE" "L2 ProxyAdmin")"
EXPECTED_L2_TOKEN_BEACON="$(compute_expected_address "$PRECOMPUTED_L2_DEPLOYER" "$EXPECTED_L2_TOKEN_BEACON_NONCE" "L2 UpgradeableBeacon")"
EXPECTED_L2_TOKEN_BRIDGE="$(compute_expected_address "$PRECOMPUTED_L2_DEPLOYER" "$EXPECTED_L2_TOKEN_BRIDGE_NONCE" "L2 TokenBridge")"

log "L1_CHAIN_ID=$L1_CHAIN_ID"
log "L1_MODE=$L1_MODE"
