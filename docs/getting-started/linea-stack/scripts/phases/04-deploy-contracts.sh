ts()   { date -u '+%Y-%m-%dT%H:%M:%SZ'; }
log()  { printf "[deploy-contracts] %s timestamp=%s\n" "$*" "$(ts)"; }
die()  { printf "[deploy-contracts] ERROR: %s timestamp=%s\n" "$*" "$(ts)" >&2; exit 1; }

validate_bool() {
  local name="$1" value="$2"
  case "$value" in
    true|false) ;;
    *) die "$name must be either true or false (got '$value')" ;;
  esac
}

[[ -f /scripts/lib/runtime.sh ]] || die "/scripts/lib/runtime.sh missing; deploy-contracts requires scripts/lib mount"
