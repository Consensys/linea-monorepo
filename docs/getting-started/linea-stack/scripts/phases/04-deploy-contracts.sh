extract_deploy_block() {
  local logfile="$1" contract_name="$2" block
  [[ -f "$logfile" ]] || die "missing deploy log for $contract_name: $logfile"
  block="$(grep -E "^contract=${contract_name} deployed: " "$logfile" | tail -1 | sed -nE 's/.*blockNumber=([0-9]+).*/\1/p')"
  [[ "$block" =~ ^[0-9]+$ ]] || die "could not extract deploy block for $contract_name from $logfile"
  printf '%s\n' "$block"
}

