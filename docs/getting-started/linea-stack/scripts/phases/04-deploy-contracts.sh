# POSIX-y JSON extraction (account-setup writes a controlled shape).
json_field()  { sed -nE "s/.*\"$1\":[[:space:]]*\"([^\"]+)\".*/\1/p" "$PRECOMPUTED" | head -1; }
json_int()    { sed -nE "s/.*\"$1\":[[:space:]]*([0-9]+).*/\1/p" "$PRECOMPUTED" | head -1; }

L1_CHAIN_ID="$(json_field l1ChainId)"
