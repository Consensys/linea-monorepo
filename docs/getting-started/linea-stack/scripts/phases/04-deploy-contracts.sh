[[ "$L1_DEPLOYER_BALANCE_WEI" =~ ^[0-9]+$ ]] \
  || die "Could not read L1 deployer balance for $L1_DEPLOYER_ADDRESS: $L1_DEPLOYER_BALANCE_WEI"

log "L1 deployer balance: $L1_DEPLOYER_BALANCE_WEI wei at $L1_DEPLOYER_ADDRESS"
if [[ "$L1_MODE" == "local" ]]; then
  log "L1 deployer required minimum: nonzero local genesis/dev balance"
else
  log "L1 deployer required minimum: $L1_DEPLOYER_MIN_BALANCE_WEI wei"
fi
log "L1 deploy gas price: $L1_DEPLOY_GAS_PRICE_WEI wei"
if [[ "$L1_MODE" == "local" && "$L1_DEPLOYER_BALANCE_WEI" == "0" ]]; then
  die "Late L1 deployer balance safety check failed. Local L1 deployer $L1_DEPLOYER_ADDRESS has zero wei; run ./scripts/reset.sh before restarting after local L1 volume deletion."
fi
if [[ "$L1_MODE" == "sepolia" ]] && wei_lt "$L1_DEPLOYER_BALANCE_WEI" "$L1_DEPLOYER_MIN_BALANCE_WEI"; then
  die "Late L1 deployer balance safety check failed. Fund $L1_DEPLOYER_ADDRESS on Sepolia to at least $L1_DEPLOYER_MIN_BALANCE_WEI wei, or lower L1_DEPLOYER_MIN_BALANCE_WEI only if you also lower the L1 runtime top-ups."
fi

# ─────────────────────────────────────────────────────────────────────────────
# Query Shomei for the L2 genesis ZK state root + compute genesis shnarf
# ─────────────────────────────────────────────────────────────────────────────
#
# `eth_getBlockByNumber(0).stateRoot` is the Merkle-Patricia root, which is
# wrong for ZK proof verification — the L1 LineaRollup verifies against
# Shomei's ZK state root, accessible via rollup_getZkEVMStateMerkleProofV0.
# Shomei must be up; it depends on l2-node-besu so by the time this script
# runs (after sequencer + l2-node-besu healthy) Shomei is reachable.

step "Query Shomei for genesis state root + compute shnarf"

SHOMEI_URL="${SHOMEI_URL:-http://shomei:8888}"

# Wait for Shomei to be reachable + return data on block 0.
log "Waiting for Shomei at $SHOMEI_URL"
SHOMEI_RESP=""
for _ in $(seq 1 60); do
  SHOMEI_RESP=$(curl -fsS --max-time 5 -X POST -H "Content-Type: application/json" \
    -d '{"jsonrpc":"2.0","id":1,"method":"rollup_getZkEVMStateMerkleProofV0","params":[{"startBlockNumber":0,"endBlockNumber":0,"zkStateManagerVersion":"2.3.0"}]}' \
    "$SHOMEI_URL" 2>/dev/null || true)
  if [[ -n "$SHOMEI_RESP" ]] && echo "$SHOMEI_RESP" | grep -q "zkEndStateRootHash"; then
    break
  fi
