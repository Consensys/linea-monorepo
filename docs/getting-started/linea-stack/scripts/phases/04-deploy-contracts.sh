[[ "$PRECOMPUTED_LINEA_ROLLUP" =~ ^0x[a-fA-F0-9]{40}$ ]] || die "Could not extract l1.LineaRollupV8 from $PRECOMPUTED"
[[ "$PRECOMPUTED_L2_MS" =~ ^0x[a-fA-F0-9]{40}$ ]] || die "Could not extract l2.L2MessageService from $PRECOMPUTED"
[[ "$PRECOMPUTED_L1_BLOB_SUBMITTER" =~ ^0x[a-fA-F0-9]{40}$ ]] || die "Could not extract signers.l1BlobSubmitterAddress from $PRECOMPUTED"
[[ "$PRECOMPUTED_L1_FINALIZATION_SUBMITTER" =~ ^0x[a-fA-F0-9]{40}$ ]] || die "Could not extract signers.l1FinalizationSubmitterAddress from $PRECOMPUTED"
[[ "$PRECOMPUTED_L1_POSTMAN" =~ ^0x[a-fA-F0-9]{40}$ ]] || die "Could not extract signers.l1PostmanAddress from $PRECOMPUTED"
[[ "$PRECOMPUTED_L2_DEPLOYER" =~ ^0x[a-fA-F0-9]{40}$ ]] || die "Could not extract signers.l2DeployerAddress from $PRECOMPUTED"
[[ "$PRECOMPUTED_L2_MESSAGE_ANCHORING" =~ ^0x[a-fA-F0-9]{40}$ ]] || die "Could not extract signers.l2MessageAnchoringAddress from $PRECOMPUTED"
[[ "$PRECOMPUTED_L2_POSTMAN" =~ ^0x[a-fA-F0-9]{40}$ ]] || die "Could not extract signers.l2PostmanAddress from $PRECOMPUTED"

compute_expected_address() {
  local deployer="$1" nonce="$2" label="$3" address
  address="$(cast compute-address "$deployer" --nonce "$nonce" | awk '{print $NF}')"
  [[ "$address" =~ ^0x[a-fA-F0-9]{40}$ ]] || die "Could not compute expected $label address"
  printf '%s' "$address"
}

if [[ "$DEPLOY_FORCED_TRANSACTION_GATEWAY" == "true" ]]; then
  L1_TOKEN_BRIDGE_NONCE_OFFSET=8
else
  L1_TOKEN_BRIDGE_NONCE_OFFSET=5
