#!/usr/bin/env bash
#
# deploy-contracts.sh — Linea stack v0 contract deployment wrapper.
#
# Runs the boot-critical deploy targets from `contracts/makefile-contracts.mk`
# in serial. Demo ERC20 contracts are deployed on demand by traffic/smoke
# scripts, not during normal boot.
#
#   1. deploy-linea-rollup-v8       (L1)  → emits LINEA_ROLLUP_ADDRESS
#   2. deploy-l2messageservice      (L2)  → emits L2_MESSAGE_SERVICE_ADDRESS
#   3. deploy-token-bridge-l1       (L1)  ← consumes both forwarded addresses
#   4. deploy-token-bridge-l2       (L2)  ← consumes both forwarded addresses
# After the boot-critical deploy steps, this script:
#   - aggregates addresses into /deployments/addresses.json
#   - writes /deployments/deploy-runtime.env with non-secret deploy-time values:
#     L2 genesis state root, computed shnarf, and deploy blocks.
#
# runtime-config-finalize starts *after* this script. It renders the final
# coordinator config from /deployments deploy artifacts.
#
# See scripts/internal/DEPLOY-ENV-CONTRACT.md for the full env-var contract per step.
#
# Required env (compose injects from .env):
#   L1_MODE                               — sepolia (default) or local.
#   L1_RPC_URL                            — Sepolia HTTPS RPC, or local L1 RPC defaulted by mode.
#   L1 deployer                           — resolved by scripts/internal/deployer-wallet.ts from
#                                           local mode, generated/provided keystore, or deprecated
#                                           raw-key compatibility.
#
# Optional env:
#   LINEA_COORDINATOR_DATA_AVAILABILITY   — ROLLUP only (default)
#   L2_CHAIN_ID                           — overrides default 1337
#
# Runtime service keys are generated by 01-generate-accounts.sh and persisted
# in /accounts/runtime-keys.env.
#
set -euo pipefail

# ─────────────────────────────────────────────────────────────────────────────
# Inputs and constants
# ─────────────────────────────────────────────────────────────────────────────

