log "L1 blob submitter=$PRECOMPUTED_L1_BLOB_SUBMITTER"
log "L1 finalization submitter=$PRECOMPUTED_L1_FINALIZATION_SUBMITTER"
log "L1 postman=$PRECOMPUTED_L1_POSTMAN"
log "L2 deployer=$PRECOMPUTED_L2_DEPLOYER"
