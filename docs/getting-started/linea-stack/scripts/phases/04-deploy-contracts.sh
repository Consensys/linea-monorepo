EXPECTED_L1_TOKEN_BRIDGE_NONCE=$((EXPECTED_L1_BRIDGED_TOKEN_NONCE + 4))
EXPECTED_L2_BRIDGED_TOKEN_NONCE=$((L2_DEPLOYER_START_NONCE + 3))
EXPECTED_L2_TOKEN_BRIDGE_IMPL_NONCE=$((L2_DEPLOYER_START_NONCE + 4))
EXPECTED_L2_PROXY_ADMIN_NONCE=$((L2_DEPLOYER_START_NONCE + 5))
EXPECTED_L2_TOKEN_BEACON_NONCE=$((L2_DEPLOYER_START_NONCE + 6))
EXPECTED_L2_TOKEN_BRIDGE_NONCE=$((L2_DEPLOYER_START_NONCE + 7))

