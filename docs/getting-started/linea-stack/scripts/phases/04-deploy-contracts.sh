log "Expected L2 TokenBridge: $EXPECTED_L2_TOKEN_BRIDGE"

DEFAULT_L1_OPERATOR_ADDRESSES="$L1_DEPLOYER_ADDRESS,$PRECOMPUTED_L1_BLOB_SUBMITTER,$PRECOMPUTED_L1_FINALIZATION_SUBMITTER"
: "${L1_DEPLOYER_MIN_BALANCE_WEI:?L1_DEPLOYER_MIN_BALANCE_WEI must be passed by Compose}"
: "${L1_ROLE_MIN_BALANCE_WEI:?L1_ROLE_MIN_BALANCE_WEI must be passed by Compose}"
: "${L1_ROLE_TOP_UP_WEI:?L1_ROLE_TOP_UP_WEI must be passed by Compose}"
: "${L1_POSTMAN_MIN_BALANCE_WEI:?L1_POSTMAN_MIN_BALANCE_WEI must be passed by Compose}"
: "${L1_POSTMAN_TOP_UP_WEI:?L1_POSTMAN_TOP_UP_WEI must be passed by Compose}"
: "${L2_RUNTIME_MIN_BALANCE_WEI:?L2_RUNTIME_MIN_BALANCE_WEI must be passed by Compose}"
: "${L2_RUNTIME_TOP_UP_WEI:?L2_RUNTIME_TOP_UP_WEI must be passed by Compose}"
: "${L2_GAS_PRICE_WEI:?L2_GAS_PRICE_WEI must be passed by Compose}"

step "Late L1 deployer balance safety check"

