timing_now_ms() {
  node -e 'console.log(Date.now())'
}

TIMING_STEP_NAME=""
TIMING_STEP_STARTED_MS="$(timing_now_ms)"

timing_record() {
  local name="$1" started_ms="$2" ended_ms="$3" status="$4"
  if [[ -x /workspace/node_modules/.bin/ts-node ]]; then
    NODE_PATH="/workspace/node_modules:/workspace/contracts/node_modules${NODE_PATH:+:$NODE_PATH}" \
    TS_NODE_TRANSPILE_ONLY=1 \
    TS_NODE_COMPILER_OPTIONS='{"module":"CommonJS","moduleResolution":"Node"}' \
      pnpm -s exec ts-node /scripts/internal/deploy-timing.ts record \
        "$DEPLOY_TIMING_PATH" "$name" "$started_ms" "$ended_ms" "$status" \
      >/dev/null 2>&1 || true
  fi
}

timing_finish_current_step() {
  local status="$1" ended_ms
  [[ -n "$TIMING_STEP_NAME" ]] || return 0
  ended_ms="$(timing_now_ms)"
  timing_record "$TIMING_STEP_NAME" "$TIMING_STEP_STARTED_MS" "$ended_ms" "$status"
  TIMING_STEP_NAME=""
}

step() {
  timing_finish_current_step "ok"
  TIMING_STEP_NAME="$*"
  TIMING_STEP_STARTED_MS="$(timing_now_ms)"
  printf "\n[deploy-contracts] ===== %s ===== timestamp=%s\n" "$*" "$(ts)"
}

finish_deploy_timing() {
  local status=$?
  if [[ "$status" -eq 0 ]]; then
    timing_finish_current_step "ok"
  else
    timing_finish_current_step "failed"
  fi
  exit "$status"
}
