# ─────────────────────────────────────────────────────────────────────────────
# Step functions — one per Make target
# ─────────────────────────────────────────────────────────────────────────────
#
# Idempotency: each step writes its log to /deployments/deploy-logs/stepN-*.log
# on artifacts/deployments. If this script is re-run after a partial
# failure (e.g. step 5 failed mid-deploy), already-completed steps detect
# their prior log and skip re-deploying — otherwise the wallet's advanced
# nonce would deploy NEW contracts at NEW addresses, breaking the
# precomputed-address verify chain. To force a full re-deploy, run with a
# fresh artifact set (`./scripts/reset.sh`).

# Returns 0 if the step's log file shows a successfully-deployed contract,
# 1 otherwise. Used by each step to decide whether to skip the deploy and
# just re-extract the address from the existing log.
step_already_done() {
  local logfile="$1" contract_name="$2"
  [[ -f "$logfile" ]] && grep -qE "^contract=${contract_name} deployed: " "$logfile"
}

# Same as step_already_done, but also verifies that the logged address is the
# expected deterministic address and that code exists on the target chain. This
# protects local restarts where deploy logs survive but chain state does not.
step_already_done_with_code() {
  local logfile="$1" contract_name="$2" rpc_url="$3" chain_label="$4" expected_address="$5" label="$6"
  local address code

  if ! step_already_done "$logfile" "$contract_name"; then
    return 1
  fi

  address="$(require_address "$logfile" "$contract_name")"
  verify_address "$address" "$expected_address" "$label"
  code="$(cast code "$address" --rpc-url "$rpc_url" 2>/dev/null || true)"
  if [[ "$code" =~ ^0x[0-9a-fA-F]+$ && "$code" != "0x" ]]; then
    log "$contract_name: prior $chain_label deploy log verified at expected address with on-chain code: $address"
    return 0
  fi

  log "$contract_name: prior $chain_label deploy log present but no code at $address — redeploying"
  return 1
}

verify_step1_gateway_mode() {
