// Package codegen generates a Zig verifier source file from a compiled
// prover-ray wiop.System. The type dispatch on VerifierAction happens here in
// Go at codegen time, so the generated Zig is a flat, concrete sequence of
// verification steps with no runtime dispatch.
package codegen

import (
	"fmt"
	"io"
	"strings"

	"github.com/consensys/linea-monorepo/prover-ray/maths/koalabear/field"
	"github.com/consensys/linea-monorepo/prover-ray/wiop"
	"github.com/consensys/linea-monorepo/prover-ray/wiop/compilers/global"
)

// GenerateZig writes a Zig source file to dst implementing verifyGenerated
// from the compiled sys.
//
// sys must be fully compiled before calling GenerateZig.
func GenerateZig(sys *wiop.System, dst io.Writer) error {
	colFlatIdx := buildColFlatIdx(sys)
	cellAccessMap := buildCellAccessMap(sys)
	colTotal, cellTotal, evalCellTotal := proofPayloadBounds(sys)
	cellKinds := buildCellKinds(sys)
	w := &writer{}

	w.line("// Code generated by verifier-ray/codegen. DO NOT EDIT.")
	w.line("//")
	w.line("// This file is the output of cmd/ray-zig-codegen, which calls GenerateZig on a")
	w.line("// compiled wiop.System and writes a flat, concrete Zig verification sequence.")
	w.line("//")
	w.line("// Current state: generated from a placeholder circuit (see buildSystem in")
	w.line("// codegen/cmd/ray-zig-codegen/main.go). When the real prover-ray circuit is")
	w.line("// wired in, main.go's buildSystem will be replaced with the actual compiled")
	w.line("// wiop.System. Regenerate this file with:")
	w.line("//")
	w.line("//   make generate-stub")
	w.blank()
	w.line(`const runtime = @import("../runtime.zig");`)
	w.line(`const proof_mod = @import("../proof.zig");`)
	w.line(`const gc = @import("../query/global_constraint.zig");`)
	w.line(`const ext = @import("../field/koalabear_ext.zig");`)
	w.line(`const base = @import("../field/koalabear.zig");`)
	w.blank()
	w.line("pub const round_count: usize = %d;", len(sys.Rounds))
	w.line("pub const min_columns: usize = %d;", colTotal)
	w.line("pub const min_cells: usize = %d;", cellTotal)
	w.line("pub const min_eval_cells: usize = %d;", evalCellTotal)
	w.line("pub const cell_is_extension = [_]bool{%s};", formatBoolLiterals(cellKinds))
	w.blank()
	w.line("const Error = runtime.Error || gc.Error || base.Error;")
	w.blank()
	w.line("pub fn verifyGenerated(rt: *runtime.Runtime, proof: proof_mod.Proof) Error!void {")
	w.in()

	colBase, cellBase := 0, 0
	gvIdx := 0
	for roundNr := range sys.Rounds {
		r := sys.Rounds[roundNr]
		w.blank()
		w.line("// === Round %d ===", roundNr)

		if roundNr < len(sys.Rounds)-1 {
			nextNumCoins := len(sys.Rounds[roundNr+1].Coins)
			emitAdvanceRound(countVisibleColumns(r.Columns), len(r.Cells), nextNumCoins, roundNr, colBase, cellBase, w)
		}

		for _, va := range r.VerifierActions {
			gv, ok := va.(*global.Verifier)
			if !ok {
				return fmt.Errorf("codegen: round %d: unsupported verifier action %T", roundNr, va)
			}
			if err := emitGlobalVerify(gv, colFlatIdx, cellAccessMap, gvIdx, w); err != nil {
				return err
			}
			gvIdx++
		}

		colBase += countVisibleColumns(r.Columns)
		cellBase += len(r.Cells)
	}

	w.out()
	w.line("}")

	return w.writeTo(dst)
}

// proofPayloadBounds returns the minimum proof.columns, proof.cells, and
// proof.eval_cells slice lengths required by the generated verifier stub.
// Out-of-bounds eval_cell accesses in Zig are traps (not catchable errors),
// so the stub must not run against a truncated proof.
func proofPayloadBounds(sys *wiop.System) (colTotal, cellTotal, evalCellTotal int) {
	for roundNr, r := range sys.Rounds {
		colTotal += countVisibleColumns(r.Columns)
		// Only non-last rounds contribute to proof.cells via advanceRoundWithMessage;
		// the last round's cells are eval claims accessed via proof.eval_cells.
		if roundNr < len(sys.Rounds)-1 {
			cellTotal += len(r.Cells)
		}
		for _, va := range r.VerifierActions {
			gv, ok := va.(*global.Verifier)
			if !ok {
				continue // unknown actions are rejected by GenerateZig; skip bounds contribution
			}
			for _, view := range gv.WitnessViews {
				pos := gv.WitnessClaimEvalCellPos(view)
				if pos >= 0 && pos+1 > evalCellTotal {
					evalCellTotal = pos + 1
				}
			}
			for _, bkt := range gv.ExportedBuckets() {
				if len(bkt.QuotientClaims) == 0 {
					continue
				}
				quotEnd := bkt.QuotientClaims[0].Context.ID.Position() + len(bkt.QuotientClaims)
				if quotEnd > evalCellTotal {
					evalCellTotal = quotEnd
				}
			}
		}
	}
	return
}

func buildColFlatIdx(sys *wiop.System) map[wiop.ObjectID]int {
	m := make(map[wiop.ObjectID]int)
	flat := 0
	for _, r := range sys.Rounds {
		for _, col := range r.Columns {
			if !isVerifierVisible(col) {
				continue
			}
			m[col.Context.ID] = flat
			flat++
		}
	}
	return m
}

// buildCellAccessMap returns a map from each verifier-visible cell's ObjectID
// to the Zig expression for reading its value from proof.cells.
// The generated verifier only carries round-message cells from non-final
// rounds in proof.cells; eval-round values are represented separately in
// proof.eval_cells through witness and quotient claims.
func buildCellAccessMap(sys *wiop.System) map[wiop.ObjectID]string {
	m := make(map[wiop.ObjectID]string)
	flat := 0
	for roundNr, r := range sys.Rounds {
		if roundNr >= len(sys.Rounds)-1 {
			break
		}
		for _, cell := range r.Cells {
			if cell.IsExtension() {
				m[cell.Context.ID] = fmt.Sprintf("proof.cells[%d].ext", flat)
			} else {
				m[cell.Context.ID] = fmt.Sprintf("ext.Ext.lift(proof.cells[%d].base)", flat)
			}
			flat++
		}
	}
	return m
}

func buildCellKinds(sys *wiop.System) []bool {
	kinds := make([]bool, 0)
	for roundNr, r := range sys.Rounds {
		if roundNr >= len(sys.Rounds)-1 {
			break
		}
		for _, cell := range r.Cells {
			kinds = append(kinds, cell.IsExtension())
		}
	}
	return kinds
}

// emitAdvanceRound emits the coin-buffer declaration and advanceRoundWithMessage
// call for round roundNr, absorbing that round's columns and cells and deriving
// coins for the next round.
func emitAdvanceRound(numCols, numCells, nextNumCoins, roundNr, colBase, cellBase int, w *writer) {
	if nextNumCoins > 0 {
		w.line("var _coins_r%d: [%d]runtime.Coin = undefined;", roundNr+1, nextNumCoins)
		w.line("const coins_r%d = try rt.advanceRoundWithMessage(%d, .{", roundNr+1, roundNr)
	} else {
		w.line("_ = try rt.advanceRoundWithMessage(%d, .{", roundNr)
	}
	w.in()
	w.line(".columns = proof.columns[%d..%d],", colBase, colBase+numCols)
	w.line(".cells = proof.cells[%d..%d],", cellBase, cellBase+numCells)
	w.line(".next_round_coin_count = %d,", nextNumCoins)
	w.out()
	if nextNumCoins > 0 {
		w.line("}, &_coins_r%d);", roundNr+1)
	} else {
		w.line("}, &.{});")
	}
}

// emitGlobalVerify emits Steps 1–5 for one global.Verifier:
//   - Steps 1+2 (expression eval + cancellation) are inlined as Zig arithmetic.
//   - Steps 3–5 (aggregate + quotient identity) are delegated to gc.verify.
//
// gvIdx is a unique index used to avoid variable-name collisions across modules.
func emitGlobalVerify(gv *global.Verifier, colFlatIdx map[wiop.ObjectID]int, cellAccessMap map[wiop.ObjectID]string, gvIdx int, w *writer) error {
	mc := gv.MergeCoin.Context.ID
	ec := gv.EvalCoin.Context.ID

	nVar := fmt.Sprintf("_n%d", gvIdx)
	if gv.Module.IsDynamic() {
		cols := gv.Module.Columns
		if len(cols) == 0 {
			return fmt.Errorf("dynamic module %q has no columns", gv.Module.Context.Path())
		}
		w.line("const %s = blk_%d: {", nVar, gvIdx)
		w.in()
		w.line("var _dyn_n: usize = 0;")
		proofBackedCols := 0
		for _, col := range cols {
			if !isVerifierVisible(col) {
				return fmt.Errorf(
					"dynamic module %q contains internal column %q; verifier cannot derive runtime size from proof payload",
					gv.Module.Context.Path(),
					col.Context.Path(),
				)
			}
			flatIdx, ok := colFlatIdx[col.Context.ID]
			if !ok {
				continue
			}
			w.line("if (proof.columns[%d].assignment.len() > _dyn_n) _dyn_n = proof.columns[%d].assignment.len();", flatIdx, flatIdx)
			proofBackedCols++
		}
		if proofBackedCols == 0 {
			return fmt.Errorf("dynamic module %q has no proof-backed columns", gv.Module.Context.Path())
		}
		w.line("break :blk_%d _dyn_n;", gvIdx)
		w.out()
		w.line("}; // n (dynamic module size)")
	} else {
		w.line("const %s: usize = %d; // n (module size)", nVar, gv.Module.Size())
	}

	for bktIdx, bkt := range gv.ExportedBuckets() {
		for vIdx, v := range bkt.Vanishings {
			// Step 1: evaluate each vanishing expression at r — V_j(r) = eval_expression(V_j, witness_claims)
			exprCode, err := emitExprCode(v.Expression, gv, cellAccessMap)
			if err != nil {
				return fmt.Errorf("codegen: gv%d bucket%d vanishing%d: %w", gvIdx, bktIdx, vIdx, err)
			}
			w.line("const _v%d_%d_%d = %s; // Step 1: V_j(r)", gvIdx, bktIdx, vIdx, exprCode)

			// Step 2: apply cancellation — P_j(r) = V_j(r) * prod_{k in cancelled_j}(r - omega^k)
			cVarName := fmt.Sprintf("_c%d_%d_%d", gvIdx, bktIdx, vIdx)
			if err := emitCancellationCode(cVarName, v.CancelledPositions, gv, nVar, w); err != nil {
				return fmt.Errorf("codegen: gv%d bucket%d vanishing%d cancellation: %w", gvIdx, bktIdx, vIdx, err)
			}
			w.line("const _pc%d_%d_%d = _v%d_%d_%d.mul(_c%d_%d_%d); // Step 2: P_j(r) = V_j(r) * Cancel_j(r)",
				gvIdx, bktIdx, vIdx, gvIdx, bktIdx, vIdx, gvIdx, bktIdx, vIdx)
		}

		veVar := fmt.Sprintf("_ve%d_%d", gvIdx, bktIdx)
		w.line("const %s = [_]ext.Ext{", veVar)
		w.in()
		for vIdx := range bkt.Vanishings {
			w.line("_pc%d_%d_%d,", gvIdx, bktIdx, vIdx)
		}
		w.out()
		w.line("};")

		if len(bkt.QuotientClaims) == 0 {
			return fmt.Errorf("codegen: gv%d bucket%d has no quotient claims", gvIdx, bktIdx)
		}
		quotStart := bkt.QuotientClaims[0].Context.ID.Position()
		for i, qc := range bkt.QuotientClaims {
			if got := qc.Context.ID.Position(); got != quotStart+i {
				return fmt.Errorf("codegen: gv%d bucket%d quotient claims are not contiguous (claim %d has position %d, expected %d)", gvIdx, bktIdx, i, got, quotStart+i)
			}
		}
		quotEnd := quotStart + len(bkt.QuotientClaims)

		// Steps 3-5 are handled by gc.verify:
		//   Step 3: P_agg(r) = sum_j merge_coin^j * P_j(r)
		//   Step 4: Q(r) = sum_k r^(n*k) * Q_k(r)
		//   Step 5: assert P_agg(r) == (r^n - 1) * Q(r)
		w.line("try gc.verify(")
		w.in()
		w.line("%s,", nVar)
		w.line("coins_r%d[%d], // merge_coin", mc.Slot(), mc.Position())
		w.line("coins_r%d[%d], // r (eval point)", ec.Slot(), ec.Position())
		w.line("&%s, // vanishing_evals: P_j(r) per vanishing", veVar)
		w.line("proof.eval_cells[%d..%d], // quotient_evals Q_k(r)", quotStart, quotEnd)
		w.out()
		w.line(");")
	}

	return nil
}

// emitExprCode returns a Zig expression string for a wiop.Expression.
func emitExprCode(expr wiop.Expression, gv *global.Verifier, cellAccessMap map[wiop.ObjectID]string) (string, error) {
	switch e := expr.(type) {
	case *wiop.ColumnView:
		pos := gv.WitnessClaimEvalCellPos(e)
		if pos < 0 {
			return "", fmt.Errorf("column view (shift=%d) not in witness claim map", e.ShiftingOffset)
		}
		return fmt.Sprintf("proof.eval_cells[%d]", pos), nil

	case *wiop.Constant:
		switch v := e.Value.Uint64(); v {
		case 0:
			return "ext.Ext.zero()", nil
		case 1:
			return "ext.Ext.one()", nil
		default:
			return fmt.Sprintf("ext.Ext.lift(base.Element.fromCanonical(%d) catch unreachable)", v), nil
		}

	case *wiop.ArithmeticOperation:
		switch e.Operator {
		case wiop.ArithmeticOperatorAdd,
			wiop.ArithmeticOperatorSub,
			wiop.ArithmeticOperatorMul:
			left, err := emitExprCode(e.Operands[0], gv, cellAccessMap)
			if err != nil {
				return "", err
			}
			right, err := emitExprCode(e.Operands[1], gv, cellAccessMap)
			if err != nil {
				return "", err
			}
			ops := map[wiop.ArithmeticOperator]string{
				wiop.ArithmeticOperatorAdd: "add",
				wiop.ArithmeticOperatorSub: "sub",
				wiop.ArithmeticOperatorMul: "mul",
			}
			return fmt.Sprintf("(%s).%s(%s)", left, ops[e.Operator], right), nil

		case wiop.ArithmeticOperatorDiv:
			left, err := emitExprCode(e.Operands[0], gv, cellAccessMap)
			if err != nil {
				return "", err
			}
			right, err := emitExprCode(e.Operands[1], gv, cellAccessMap)
			if err != nil {
				return "", err
			}
			return fmt.Sprintf("try (%s).div(%s)", left, right), nil

		case wiop.ArithmeticOperatorDouble:
			operand, err := emitExprCode(e.Operands[0], gv, cellAccessMap)
			if err != nil {
				return "", err
			}
			// no .double() on ext.Ext; capture in a block to avoid evaluating operand twice
			return fmt.Sprintf("(blk: { const _d = %s; break :blk _d.add(_d); })", operand), nil

		case wiop.ArithmeticOperatorSquare:
			operand, err := emitExprCode(e.Operands[0], gv, cellAccessMap)
			if err != nil {
				return "", err
			}
			return fmt.Sprintf("(%s).square()", operand), nil

		case wiop.ArithmeticOperatorNegate:
			operand, err := emitExprCode(e.Operands[0], gv, cellAccessMap)
			if err != nil {
				return "", err
			}
			return fmt.Sprintf("(%s).neg()", operand), nil

		case wiop.ArithmeticOperatorInverse:
			operand, err := emitExprCode(e.Operands[0], gv, cellAccessMap)
			if err != nil {
				return "", err
			}
			return fmt.Sprintf("try (%s).inverse()", operand), nil

		default:
			return "", fmt.Errorf("unsupported arithmetic operator %v", e.Operator)
		}

	case *wiop.Cell:
		expr, ok := cellAccessMap[e.Context.ID]
		if !ok {
			return "", fmt.Errorf("cell at round %d position %d not in payload map", e.Context.ID.Slot(), e.Context.ID.Position())
		}
		return expr, nil

	case *wiop.CoinField:
		id := e.Context.ID
		if id.Slot() == 0 {
			return "", fmt.Errorf("coin %q is declared in round 0 and has no derived coin slice", e.Context.Path())
		}
		return fmt.Sprintf("coins_r%d[%d]", id.Slot(), id.Position()), nil

	default:
		return "", fmt.Errorf("unsupported expression type %T", expr)
	}
}

// emitCancellationCode emits a Zig const declaration for the cancellation
// polynomial C(r) = Π_{k ∈ cancelled}(r − ω^k).
//
// Static modules: all ω^k are computed at Go codegen time and emitted as
// field constants — no gc.evalCancellation call at all.
//
// Dynamic modules: position 0 (ω^0 = 1) is always inlined as (r − 1).
// Any non-zero positions are passed to gc.evalCancellation. Mixed cases like
// [0, -1] split: the row-0 factor is inlined and only the non-zero positions
// go to evalCancellation, which is then multiplied with the inlined factor.
func emitCancellationCode(varName string, cancelled []int, gv *global.Verifier, nVar string, w *writer) error {
	ec := gv.EvalCoin.Context.ID
	rExpr := fmt.Sprintf("coins_r%d[%d]", ec.Slot(), ec.Position())

	if len(cancelled) == 0 {
		w.line("const %s = ext.Ext.one(); // Step 2: Cancel_j(r) = 1 (no cancelled rows)", varName)
		return nil
	}

	if !gv.Module.IsDynamic() {
		// Static module: all ω^k are known at codegen time — inline the full product.
		n := gv.Module.Size()
		omega := field.RootOfUnityBy(n)
		factors := make([]string, len(cancelled))
		for i, pos := range cancelled {
			normalized := pos
			if normalized < 0 {
				normalized = n + normalized
			}
			var omegaK field.Element
			field.ExpToInt(&omegaK, omega, normalized)
			factors[i] = cancellationFactor(rExpr, omegaK.Uint64())
		}
		w.line("const %s = %s; // Step 2: Cancel_j(r) (static, ω^k inlined)", varName, mulChain(factors))
		return nil
	}

	// Dynamic module: separate zero positions (inlineable) from non-zero positions
	// (need gc.evalCancellation). ω^0 = 1 regardless of n, so (r - 1) is always safe.
	zeroCount := 0
	var nonZero []int
	for _, pos := range cancelled {
		if pos == 0 {
			zeroCount++
		} else {
			nonZero = append(nonZero, pos)
		}
	}

	// Build the inlined part: (r - 1)^zeroCount
	var inlinedFactors []string
	for range zeroCount {
		inlinedFactors = append(inlinedFactors, cancellationFactor(rExpr, 1))
	}

	if len(nonZero) == 0 {
		// All positions are 0 — fully inlineable.
		w.line("const %s = %s; // Step 2: Cancel_j(r) (row 0, inlined)", varName, mulChain(inlinedFactors))
		return nil
	}

	// Some non-zero positions: emit gc.evalCancellation for those, multiply with
	// any inlined row-0 factors.
	positions := formatCancelledPositions(nonZero)
	evalExpr := fmt.Sprintf("try gc.evalCancellation(%s, &.{%s}, %s)", nVar, positions, rExpr)
	if len(inlinedFactors) == 0 {
		w.line("const %s = %s; // Step 2: Cancel_j(r)", varName, evalExpr)
	} else {
		allFactors := append(inlinedFactors, evalExpr)
		w.line("const %s = %s; // Step 2: Cancel_j(r) (row 0 inlined, rest via evalCancellation)", varName, mulChain(allFactors))
	}
	return nil
}

// cancellationFactor returns the Zig expression for (r − ω^k) given the
// canonical integer value of ω^k.
func cancellationFactor(rExpr string, omegaK uint64) string {
	switch omegaK {
	case 0:
		return rExpr // r − 0 = r
	case 1:
		return fmt.Sprintf("(%s).sub(ext.Ext.one())", rExpr)
	default:
		return fmt.Sprintf("(%s).sub(ext.Ext.lift(base.Element.fromCanonical(%d) catch unreachable))", rExpr, omegaK)
	}
}

// mulChain folds a slice of Zig expressions into a left-associative .mul chain.
func mulChain(factors []string) string {
	if len(factors) == 1 {
		return factors[0]
	}
	result := factors[0]
	for _, f := range factors[1:] {
		result = fmt.Sprintf("(%s).mul(%s)", result, f)
	}
	return result
}

// formatCancelledPositions formats cancelled positions as a comma-separated
// list of i64 literals for Zig &.{...} syntax.
func formatCancelledPositions(positions []int) string {
	parts := make([]string, len(positions))
	for i, p := range positions {
		parts[i] = fmt.Sprintf("%d", p)
	}
	return strings.Join(parts, ", ")
}

func formatBoolLiterals(values []bool) string {
	parts := make([]string, len(values))
	for i, value := range values {
		if value {
			parts[i] = "true"
		} else {
			parts[i] = "false"
		}
	}
	return strings.Join(parts, ", ")
}

func isVerifierVisible(col *wiop.Column) bool {
	return col.Visibility >= wiop.VisibilityOracle
}

func countVisibleColumns(cols []*wiop.Column) int {
	total := 0
	for _, col := range cols {
		if isVerifierVisible(col) {
			total++
		}
	}
	return total
}
