package generator

import (
	"fmt"
	"io"
	"strings"

	codegen "github.com/consensys/linea-monorepo/verifier-ray/codegen"
)

// System is the codegen input for one compiled verifier. Rounds carry
// per-round metadata; Vanishing, when non-nil, supplies the compiled
// vanishing query system and drives coin-routing values in the emitted Spec.
type System struct {
	Rounds []Round
	// Vanishing carries the compiled vanishing system. When non-nil, Generate
	// uses its RoundCoinCounts for the protocol Spec and emits full module
	// metadata into the output file. When nil, the spec has zero coins and the
	// vanishing system is emitted as an empty placeholder.
	Vanishing *codegen.VanishingSystem
}

// Round describes one verifier round in generated output.
type Round struct {
	ID              int
	VerifierActions []string
}

// Generate emits compilable Zig verifier constants.
func Generate(system System, dst io.Writer) error {
	w := &Writer{}
	w.Line("// Code generated by verifier-ray/codegen. DO NOT EDIT.")
	w.Line(`const verifier_ray = @import("verifier_ray");`)
	w.Line(`const protocol = verifier_ray.protocol;`)
	w.Line(`const vanishing = verifier_ray.query.vanishing;`)
	if system.Vanishing != nil {
		// field is only referenced inside emitted vanishing module constants.
		w.Line(`const field = verifier_ray.field.koalabear;`)
	}
	w.Line(`const verifier_mod = verifier_ray.verifier;`)
	w.Blank()

	for _, round := range system.Rounds {
		w.Line("// Round %d", round.ID)
		for _, action := range round.VerifierActions {
			w.Line("// verifier action: %s", action)
		}
	}

	coinCounts, coinOffsets, totalCoins := computeCoinRouting(system)

	if system.Vanishing != nil {
		if err := emitVanishingSystem(w, *system.Vanishing); err != nil {
			return err
		}
	}

	w.Line("pub const spec = protocol.Spec{")
	w.In()
	w.Line(".round_coin_counts = &%s,", intSliceLiteral(coinCounts))
	w.Line(".round_coin_offsets = &%s,", intSliceLiteral(coinOffsets))
	w.Line(".total_round_coins = %d,", totalCoins)
	w.Out()
	w.Line("};")
	w.Blank()

	w.Line("pub const systems = verifier_mod.Systems{")
	w.In()
	if system.Vanishing != nil {
		w.Line(".vanishing = %s,", vanishingSystemName)
	} else {
		w.Line(".vanishing = vanishing.System{")
		w.In()
		w.Line(".modules = &.{},")
		w.Line(".round_coin_counts = &%s,", intSliceLiteral(coinCounts))
		w.Line(".round_coin_offsets = &%s,", intSliceLiteral(coinOffsets))
		w.Line(".total_round_coins = %d,", totalCoins)
		w.Out()
		w.Line("},")
	}
	w.Out()
	w.Line("};")

	_, err := io.WriteString(dst, w.String())
	if err != nil {
		return fmt.Errorf("write generated Zig: %w", err)
	}
	return nil
}

// computeCoinRouting returns the three spec fields from the system.
// When system.Vanishing is non-nil, all three come directly from the already-
// computed VanishingSystem fields, keeping the length of offsets consistent with
// the N-element convention used by the Zig side (specOf in tests, vanishing.System
// in generated code). When nil, the fallback still emits the protocol-level
// layout expected by protocol.replay: one leading zero-count entry plus one
// zero-count/zero-offset entry per annotated round.
func computeCoinRouting(system System) (counts []int, offsets []int, total int) {
	if system.Vanishing != nil {
		return system.Vanishing.RoundCoinCounts,
			system.Vanishing.RoundCoinOffsets,
			system.Vanishing.TotalRoundCoins
	}
	n := len(system.Rounds) + 1
	return make([]int, n), make([]int, n), 0
}

func intSliceLiteral(values []int) string {
	if len(values) == 0 {
		return "[_]usize{}"
	}
	parts := make([]string, len(values))
	for i, v := range values {
		parts[i] = fmt.Sprintf("%d", v)
	}
	return "[_]usize{ " + strings.Join(parts, ", ") + " }"
}
