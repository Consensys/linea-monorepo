package codegen

import (
	"io"
	"text/template"
)

// SpecZigOptions configures the import and wrapping for generated protocol.Spec
// data.
type SpecZigOptions struct {
	ProtocolImport string
	ConstName      string
	EmitHeader     bool
}

func defaultSpecZigOptions() SpecZigOptions {
	return SpecZigOptions{
		ProtocolImport: `@import("../protocol/root.zig")`,
		ConstName:      "spec",
		EmitHeader:     true,
	}
}

// WriteSpecZig writes generated Zig source containing the protocol.Spec value
// for the supplied coin routing. It emits data only; Zig owns replay().
func WriteSpecZig(w io.Writer, routing CoinRouting) error {
	return WriteSpecZigWithOptions(w, routing, defaultSpecZigOptions())
}

func WriteSpecZigWithOptions(w io.Writer, routing CoinRouting, opts SpecZigOptions) error {
	tmpl, err := template.New("spec").Funcs(template.FuncMap{
		"intArray": intArray,
	}).Parse(specZigTemplate)
	if err != nil {
		return err
	}
	return tmpl.Execute(w, specZigData{Options: opts, Routing: routing})
}

type specZigData struct {
	Options SpecZigOptions
	Routing CoinRouting
}

const specZigTemplate = `{{if .Options.EmitHeader}}// Code generated by verifier-ray/testdata/generate; DO NOT EDIT.

const protocol = {{.Options.ProtocolImport}};

{{end}}pub const {{.Options.ConstName}} = protocol.Spec{
    .round_coin_counts = &[_]usize{{intArray .Routing.RoundCoinCounts}},
    .round_coin_offsets = &[_]usize{{intArray .Routing.RoundCoinOffsets}},
    .total_round_coins = {{.Routing.TotalRoundCoins}},
};
`
