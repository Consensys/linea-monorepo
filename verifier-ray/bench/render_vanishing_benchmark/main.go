package main

import (
	"bytes"
	"flag"
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"regexp"
	"strconv"
	"strings"
)

type metadata struct {
	name                string
	moduleCount         string
	dynamicModuleCount  string
	roundCount          string
	expressionCount     string
	bucketCount         string
	vanishingCount      string
	totalWitnessClaims  string
	totalQuotientClaims string
}

type benchmarkResult struct {
	caseIndex                 int
	meta                      metadata
	cycles                    uint64
	transcriptCycles          uint64
	vanishingCycles           uint64
	otherCycles               uint64
	poseidon2CompressionCount uint64
}

func main() {
	logPath := flag.String("log", "", "zkc benchmark log path")
	logDir := flag.String("log-dir", "", "directory containing per-case zkc benchmark logs")
	outPath := flag.String("out", "", "markdown output path")
	metadataPath := flag.String("metadata", "", "generated benchmark Zig fixture path")
	binPath := flag.String("bin", "", "R5 ELF path")
	jsonPath := flag.String("json", "", "zkc JSON input path")
	caseIndex := flag.Int("case-index", 0, "vanishing benchmark case index")
	caseSpec := flag.String("case-spec", "", "case selector for comparison: all, 0-10, or 0,2,5-8")
	printCases := flag.Bool("print-cases", false, "print selected case indexes from -case-spec and exit")
	releaseMode := flag.String("release", "", "Zig release mode")
	zkcMain := flag.String("zkc-main", "", "zkc RISC-V benchmark main path")
	flag.Parse()

	var metadataData []byte
	if *metadataPath != "" {
		metadataData = mustRead(*metadataPath)
	}

	if *printCases {
		if *metadataPath == "" || *caseSpec == "" {
			fatalf("-metadata and -case-spec are required with -print-cases")
		}
		metas, err := parseAllMetadata(metadataData)
		if err != nil {
			fatalf("%v", err)
		}
		cases, err := parseCaseSpec(*caseSpec, len(metas))
		if err != nil {
			fatalf("%v", err)
		}
		var parts []string
		for _, c := range cases {
			parts = append(parts, strconv.Itoa(c))
		}
		fmt.Println(strings.Join(parts, " "))
		return
	}

	if *logDir != "" || *caseSpec != "" {
		if *logDir == "" || *outPath == "" || *metadataPath == "" || *caseSpec == "" {
			fatalf("-log-dir, -out, -metadata, and -case-spec are required for comparison reports")
		}
		metas, err := parseAllMetadata(metadataData)
		if err != nil {
			fatalf("%v", err)
		}
		cases, err := parseCaseSpec(*caseSpec, len(metas))
		if err != nil {
			fatalf("%v", err)
		}
		results, err := readComparisonResults(*logDir, metas, cases)
		if err != nil {
			fatalf("%v", err)
		}
		renderComparisonReport(*outPath, *logDir, *caseSpec, *releaseMode, *zkcMain, results)
		return
	}

	if *logPath == "" || *outPath == "" || *metadataPath == "" {
		fatalf("-log, -out, and -metadata are required")
	}

	logData := mustRead(*logPath)
	resultCycles, err := parseBenchmarkCycles(string(logData))
	if err != nil {
		fatalf("%v", err)
	}

	meta, err := parseMetadata(metadataData, *caseIndex)
	if err != nil {
		fatalf("%v", err)
	}

	var out bytes.Buffer
	fmt.Fprintln(&out, "# Vanishing RISC-V Benchmark")
	fmt.Fprintln(&out)
	fmt.Fprintln(&out, "<!-- Code generated by make bench-vanishing-doc; DO NOT EDIT. -->")
	fmt.Fprintln(&out)
	fmt.Fprintln(&out, "Generated by `make bench-vanishing-doc`.")
	fmt.Fprintln(&out)
	fmt.Fprintln(&out, "This measures the RISC-V instruction count for the typed vanishing benchmark guest. The proof/system data is embedded as generated typed Zig fixture data. The report splits transcript coin replay from `vanishing.verify`; both are measured inside the same R5 run.")
	fmt.Fprintln(&out)
	fmt.Fprintln(&out, "## Result")
	fmt.Fprintln(&out)
	fmt.Fprintln(&out, "| Metric | Value |")
	fmt.Fprintln(&out, "| --- | ---: |")
	fmt.Fprintf(&out, "| RISC-V cycles | %s |\n", formatThousands(resultCycles.cycles))
	fmt.Fprintf(&out, "| Transcript replay cycles | %s |\n", formatThousands(resultCycles.transcriptCycles))
	fmt.Fprintf(&out, "| Poseidon2 compressions in transcript | %s |\n", formatThousands(resultCycles.poseidon2CompressionCount))
	fmt.Fprintf(&out, "| Transcript cycles / Poseidon2 compression | %s |\n", formatCyclesPerCompression(resultCycles.transcriptCycles, resultCycles.poseidon2CompressionCount))
	fmt.Fprintf(&out, "| Vanishing verify cycles | %s |\n", formatThousands(resultCycles.vanishingCycles))
	fmt.Fprintf(&out, "| Benchmark overhead / markers | %s |\n", formatThousands(resultCycles.otherCycles))
	fmt.Fprintf(&out, "| Case index | %d |\n", *caseIndex)
	fmt.Fprintf(&out, "| Case name | `%s` |\n", meta.name)
	fmt.Fprintf(&out, "| Modules | %s |\n", meta.moduleCount)
	fmt.Fprintf(&out, "| Dynamic modules | %s |\n", meta.dynamicModuleCount)
	fmt.Fprintf(&out, "| Rounds | %s |\n", meta.roundCount)
	fmt.Fprintf(&out, "| Expressions | %s |\n", meta.expressionCount)
	fmt.Fprintf(&out, "| Buckets | %s |\n", meta.bucketCount)
	fmt.Fprintf(&out, "| Vanishings | %s |\n", meta.vanishingCount)
	fmt.Fprintf(&out, "| Witness claims | %s |\n", meta.totalWitnessClaims)
	fmt.Fprintf(&out, "| Quotient claims | %s |\n", meta.totalQuotientClaims)
	if *releaseMode != "" {
		fmt.Fprintf(&out, "| Zig release mode | `%s` |\n", *releaseMode)
	}
	if *binPath != "" {
		fmt.Fprintf(&out, "| R5 ELF size | %s bytes |\n", formatThousands(fileSize(*binPath)))
	}
	if *jsonPath != "" {
		fmt.Fprintf(&out, "| zkc JSON size | %s bytes |\n", formatThousands(fileSize(*jsonPath)))
	}
	fmt.Fprintln(&out)
	fmt.Fprintln(&out, "## Environment")
	fmt.Fprintln(&out)
	fmt.Fprintln(&out, "| Tool | Value |")
	fmt.Fprintln(&out, "| --- | --- |")
	fmt.Fprintf(&out, "| Git commit | `%s` |\n", commandText("git", "rev-parse", "--short", "HEAD"))
	fmt.Fprintf(&out, "| Zig | `%s` |\n", commandText("zig", "version"))
	fmt.Fprintf(&out, "| zkc | `%s` |\n", zkcVersion())
	if *zkcMain != "" {
		fmt.Fprintf(&out, "| zkc main | `%s` |\n", *zkcMain)
	}
	fmt.Fprintf(&out, "| Log | `%s` |\n", *logPath)
	fmt.Fprintln(&out)
	fmt.Fprintln(&out, "## Reproduce")
	fmt.Fprintln(&out)
	fmt.Fprintln(&out, "```bash")
	fmt.Fprintf(&out, "cd verifier-ray\n")
	fmt.Fprintf(&out, "make bench-vanishing-doc VANISHING_BENCH_CASE=%d VANISHING_BENCH_RELEASE=%s\n", *caseIndex, *releaseMode)
	fmt.Fprintln(&out, "```")

	if err := os.MkdirAll(filepath.Dir(*outPath), 0o755); err != nil {
		fatalf("create output dir: %v", err)
	}
	if err := os.WriteFile(*outPath, out.Bytes(), 0o644); err != nil {
		fatalf("write %s: %v", *outPath, err)
	}
}

func mustRead(path string) []byte {
	data, err := os.ReadFile(path)
	if err != nil {
		fatalf("read %s: %v", path, err)
	}
	return data
}

func parseBenchmarkCycles(log string) (benchmarkResult, error) {
	cycles, err := parseOutput(log, "cycle_count")
	if err != nil {
		return benchmarkResult{}, err
	}
	transcriptStart, err := parseOutput(log, "transcript_start_cycle")
	if err != nil {
		return benchmarkResult{}, err
	}
	transcriptEnd, err := parseOutput(log, "transcript_end_cycle")
	if err != nil {
		return benchmarkResult{}, err
	}
	vanishingEnd, err := parseOutput(log, "vanishing_end_cycle")
	if err != nil {
		return benchmarkResult{}, err
	}
	poseidon2CompressionCount, err := parseOutput(log, "poseidon2_compression_count")
	if err != nil {
		return benchmarkResult{}, err
	}
	if transcriptStart >= transcriptEnd {
		return benchmarkResult{}, fmt.Errorf("invalid benchmark phase markers: transcript_start_cycle=%d transcript_end_cycle=%d", transcriptStart, transcriptEnd)
	}
	if transcriptEnd >= vanishingEnd {
		return benchmarkResult{}, fmt.Errorf("invalid benchmark phase markers: transcript_end_cycle=%d vanishing_end_cycle=%d", transcriptEnd, vanishingEnd)
	}
	if vanishingEnd > cycles {
		return benchmarkResult{}, fmt.Errorf("invalid benchmark phase markers: vanishing_end_cycle=%d cycle_count=%d", vanishingEnd, cycles)
	}

	transcriptCycles := transcriptEnd - transcriptStart - 1
	vanishingCycles := vanishingEnd - transcriptEnd - 1
	measuredPhaseCycles := transcriptCycles + vanishingCycles
	otherCycles := cycles - measuredPhaseCycles

	return benchmarkResult{
		cycles:                    cycles,
		transcriptCycles:          transcriptCycles,
		vanishingCycles:           vanishingCycles,
		otherCycles:               otherCycles,
		poseidon2CompressionCount: poseidon2CompressionCount,
	}, nil
}

func parseOutput(log string, name string) (uint64, error) {
	reOutput := regexp.MustCompile(regexp.QuoteMeta(name) + `\s*=\s*0x([0-9a-fA-F]+)`)
	if matches := reOutput.FindAllStringSubmatch(log, -1); len(matches) > 0 {
		return strconv.ParseUint(matches[len(matches)-1][1], 16, 64)
	}
	return 0, fmt.Errorf("no %s output found in benchmark log", name)
}

func parseCycles(log string) (uint64, error) {
	if cycles, err := parseOutput(log, "cycle_count"); err == nil {
		return cycles, nil
	}
	reFinal := regexp.MustCompile(`RISC-V cycles:\s*(\d+)`)
	if matches := reFinal.FindAllStringSubmatch(log, -1); len(matches) > 0 {
		return strconv.ParseUint(matches[len(matches)-1][1], 10, 64)
	}

	reTrace := regexp.MustCompile(`clock cycle:\s*(\d+)`)
	if matches := reTrace.FindAllStringSubmatch(log, -1); len(matches) > 0 {
		return strconv.ParseUint(matches[len(matches)-1][1], 10, 64)
	}
	return 0, fmt.Errorf("no RISC-V cycle count found in %s", "benchmark log")
}

func parseMetadata(data []byte, caseIndex int) (metadata, error) {
	metas, err := parseAllMetadata(data)
	if err != nil {
		return metadata{}, err
	}
	if caseIndex < 0 || caseIndex >= len(metas) {
		return metadata{}, fmt.Errorf("case index %d outside metadata range 0..%d", caseIndex, len(metas)-1)
	}
	return metas[caseIndex], nil
}

func parseAllMetadata(data []byte) ([]metadata, error) {
	var lines []string
	inMetadata := false
	for _, line := range strings.Split(string(data), "\n") {
		if strings.Contains(line, "pub const metadata") {
			inMetadata = true
			continue
		}
		if inMetadata && strings.TrimSpace(line) == "};" {
			break
		}
		if inMetadata && strings.Contains(line, ".name =") {
			lines = append(lines, line)
		}
	}
	if len(lines) == 0 {
		return nil, fmt.Errorf("no benchmark metadata found")
	}

	metas := make([]metadata, 0, len(lines))
	for i, line := range lines {
		meta, err := parseMetadataLine(line)
		if err != nil {
			return nil, fmt.Errorf("parse metadata case %d: %w", i, err)
		}
		metas = append(metas, meta)
	}
	return metas, nil
}

func parseMetadataLine(line string) (metadata, error) {
	name, err := strconv.Unquote(`"` + field(line, `\.name = "((?:\\.|[^"])*)"`) + `"`)
	if err != nil {
		return metadata{}, err
	}
	return metadata{
		name:                name,
		moduleCount:         field(line, `\.module_count = (\d+)`),
		dynamicModuleCount:  field(line, `\.dynamic_module_count = (\d+)`),
		roundCount:          field(line, `\.round_count = (\d+)`),
		expressionCount:     field(line, `\.expression_count = (\d+)`),
		bucketCount:         field(line, `\.bucket_count = (\d+)`),
		vanishingCount:      field(line, `\.vanishing_count = (\d+)`),
		totalWitnessClaims:  field(line, `\.total_witness_claims = (\d+)`),
		totalQuotientClaims: field(line, `\.total_quotient_claims = (\d+)`),
	}, nil
}

func parseCaseSpec(spec string, caseCount int) ([]int, error) {
	spec = strings.TrimSpace(spec)
	if spec == "" {
		return nil, fmt.Errorf("empty case selector")
	}
	if strings.EqualFold(spec, "all") {
		cases := make([]int, caseCount)
		for i := range cases {
			cases[i] = i
		}
		return cases, nil
	}

	seen := make(map[int]bool)
	var cases []int
	for _, token := range strings.Split(spec, ",") {
		token = strings.TrimSpace(token)
		if token == "" {
			return nil, fmt.Errorf("empty token in case selector %q", spec)
		}
		if strings.Contains(token, "-") {
			parts := strings.Split(token, "-")
			if len(parts) != 2 {
				return nil, fmt.Errorf("invalid case range %q", token)
			}
			start, err := parseCaseIndex(parts[0], caseCount)
			if err != nil {
				return nil, fmt.Errorf("invalid range start %q: %w", token, err)
			}
			end, err := parseCaseIndex(parts[1], caseCount)
			if err != nil {
				return nil, fmt.Errorf("invalid range end %q: %w", token, err)
			}
			if start > end {
				return nil, fmt.Errorf("invalid descending case range %q", token)
			}
			for i := start; i <= end; i++ {
				if !seen[i] {
					seen[i] = true
					cases = append(cases, i)
				}
			}
			continue
		}

		index, err := parseCaseIndex(token, caseCount)
		if err != nil {
			return nil, err
		}
		if !seen[index] {
			seen[index] = true
			cases = append(cases, index)
		}
	}
	if len(cases) == 0 {
		return nil, fmt.Errorf("case selector %q selected no cases", spec)
	}
	return cases, nil
}

func parseCaseIndex(value string, caseCount int) (int, error) {
	value = strings.TrimSpace(value)
	index, err := strconv.Atoi(value)
	if err != nil {
		return 0, fmt.Errorf("invalid case index %q", value)
	}
	if index < 0 || index >= caseCount {
		return 0, fmt.Errorf("case index %d outside metadata range 0..%d", index, caseCount-1)
	}
	return index, nil
}

func readComparisonResults(logDir string, metas []metadata, cases []int) ([]benchmarkResult, error) {
	results := make([]benchmarkResult, 0, len(cases))
	for _, caseIndex := range cases {
		logPath := filepath.Join(logDir, fmt.Sprintf("case-%d.log", caseIndex))
		result, err := parseBenchmarkCycles(string(mustRead(logPath)))
		if err != nil {
			return nil, fmt.Errorf("case %d: %w", caseIndex, err)
		}
		result.caseIndex = caseIndex
		result.meta = metas[caseIndex]
		results = append(results, result)
	}
	return results, nil
}

func renderComparisonReport(outPath, logDir, caseSpec, releaseMode, zkcMain string, results []benchmarkResult) {
	if len(results) == 0 {
		fatalf("no benchmark results to render")
	}

	minResult := results[0]
	maxResult := results[0]
	for _, result := range results[1:] {
		if result.cycles < minResult.cycles {
			minResult = result
		}
		if result.cycles > maxResult.cycles {
			maxResult = result
		}
	}

	var out bytes.Buffer
	fmt.Fprintln(&out, "# Vanishing RISC-V Benchmark Comparison")
	fmt.Fprintln(&out)
	fmt.Fprintln(&out, "<!-- Code generated by make bench-vanishing-compare-doc; DO NOT EDIT. -->")
	fmt.Fprintln(&out)
	fmt.Fprintln(&out, "Generated by `make bench-vanishing-compare-doc`.")
	fmt.Fprintln(&out)
	fmt.Fprintln(&out, "Each row measures one generated honest vanishing benchmark case. The zkc RISC-V runner records phase-marker checkpoints so transcript coin replay and `vanishing.verify` are reported separately.")
	fmt.Fprintln(&out)
	fmt.Fprintln(&out, "## Summary")
	fmt.Fprintln(&out)
	fmt.Fprintln(&out, "| Metric | Value |")
	fmt.Fprintln(&out, "| --- | --- |")
	fmt.Fprintf(&out, "| Case selector | `%s` |\n", caseSpec)
	fmt.Fprintf(&out, "| Cases measured | %d |\n", len(results))
	fmt.Fprintf(&out, "| Fastest | `%d %s` (%s cycles) |\n", minResult.caseIndex, markdownCell(minResult.meta.name), formatThousands(minResult.cycles))
	fmt.Fprintf(&out, "| Slowest | `%d %s` (%s cycles) |\n", maxResult.caseIndex, markdownCell(maxResult.meta.name), formatThousands(maxResult.cycles))
	if releaseMode != "" {
		fmt.Fprintf(&out, "| Zig release mode | `%s` |\n", releaseMode)
	}
	fmt.Fprintf(&out, "| Git commit | `%s` |\n", commandText("git", "rev-parse", "--short", "HEAD"))
	fmt.Fprintf(&out, "| Zig | `%s` |\n", commandText("zig", "version"))
	fmt.Fprintf(&out, "| zkc | `%s` |\n", zkcVersion())
	if zkcMain != "" {
		fmt.Fprintf(&out, "| zkc main | `%s` |\n", zkcMain)
	}
	fmt.Fprintf(&out, "| Logs | `%s/case-<index>.log` |\n", logDir)
	fmt.Fprintln(&out)
	fmt.Fprintln(&out, "## Results")
	fmt.Fprintln(&out)
	fmt.Fprintln(&out, "| Case | Scenario | RISC-V cycles | Transcript | Poseidon2 comps | Transcript / comp | Vanishing | Other | vs fastest | Modules | Dynamic | Rounds | Expressions | Buckets | Vanishings | Witness | Quotient |")
	fmt.Fprintln(&out, "| ---: | --- | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: |")
	for _, result := range results {
		fmt.Fprintf(
			&out,
			"| %d | `%s` | %s | %s | %s | %s | %s | %s | %s | %s | %s | %s | %s | %s | %s | %s | %s |\n",
			result.caseIndex,
			markdownCell(result.meta.name),
			formatThousands(result.cycles),
			formatThousands(result.transcriptCycles),
			formatThousands(result.poseidon2CompressionCount),
			formatCyclesPerCompression(result.transcriptCycles, result.poseidon2CompressionCount),
			formatThousands(result.vanishingCycles),
			formatThousands(result.otherCycles),
			formatCycleDelta(result.cycles, minResult.cycles),
			result.meta.moduleCount,
			result.meta.dynamicModuleCount,
			result.meta.roundCount,
			result.meta.expressionCount,
			result.meta.bucketCount,
			result.meta.vanishingCount,
			result.meta.totalWitnessClaims,
			result.meta.totalQuotientClaims,
		)
	}
	fmt.Fprintln(&out)
	fmt.Fprintln(&out, "## Reproduce")
	fmt.Fprintln(&out)
	fmt.Fprintln(&out, "```bash")
	fmt.Fprintln(&out, "cd verifier-ray")
	fmt.Fprintf(&out, "make bench-vanishing-compare-doc VANISHING_BENCH_CASES=%s VANISHING_BENCH_RELEASE=%s\n", shellQuote(caseSpec), shellQuote(releaseMode))
	fmt.Fprintln(&out, "```")

	if err := os.MkdirAll(filepath.Dir(outPath), 0o755); err != nil {
		fatalf("create output dir: %v", err)
	}
	if err := os.WriteFile(outPath, out.Bytes(), 0o644); err != nil {
		fatalf("write %s: %v", outPath, err)
	}
}

func field(line, pattern string) string {
	re := regexp.MustCompile(pattern)
	matches := re.FindStringSubmatch(line)
	if len(matches) != 2 {
		return "unknown"
	}
	return matches[1]
}

func fileSize(path string) uint64 {
	info, err := os.Stat(path)
	if err != nil {
		return 0
	}
	return uint64(info.Size())
}

func commandText(name string, args ...string) string {
	out, err := exec.Command(name, args...).Output()
	if err != nil {
		return "unavailable"
	}
	return strings.TrimSpace(string(out))
}

func zkcVersion() string {
	zkcPath, err := exec.LookPath("zkc")
	if err != nil {
		return "unavailable"
	}
	version := commandText("go", "version", "-m", zkcPath)
	if version == "unavailable" {
		return zkcPath
	}
	lines := strings.Split(version, "\n")
	for _, line := range lines {
		line = strings.TrimSpace(line)
		if strings.HasPrefix(line, "mod\t") {
			return line
		}
	}
	return zkcPath
}

func formatThousands[T ~uint64](value T) string {
	s := strconv.FormatUint(uint64(value), 10)
	if len(s) <= 3 {
		return s
	}
	var b strings.Builder
	prefix := len(s) % 3
	if prefix == 0 {
		prefix = 3
	}
	b.WriteString(s[:prefix])
	for i := prefix; i < len(s); i += 3 {
		b.WriteByte(',')
		b.WriteString(s[i : i+3])
	}
	return b.String()
}

func formatCyclesPerCompression(cycles uint64, compressionCount uint64) string {
	if compressionCount == 0 {
		return "n/a"
	}
	return fmt.Sprintf("%.2f", float64(cycles)/float64(compressionCount))
}

func formatCycleDelta(cycles, baseline uint64) string {
	if cycles == baseline {
		return "fastest"
	}
	delta := cycles - baseline
	percent := 100 * float64(delta) / float64(baseline)
	return fmt.Sprintf("+%s (%.1f%%)", formatThousands(delta), percent)
}

func markdownCell(value string) string {
	return strings.ReplaceAll(value, "|", `\|`)
}

func shellQuote(value string) string {
	if value == "" {
		return "''"
	}
	for _, r := range value {
		if (r >= 'a' && r <= 'z') || (r >= 'A' && r <= 'Z') || (r >= '0' && r <= '9') || strings.ContainsRune("-_./", r) {
			continue
		}
		return "'" + strings.ReplaceAll(value, "'", `'\''`) + "'"
	}
	return value
}

func fatalf(format string, args ...any) {
	fmt.Fprintf(os.Stderr, format+"\n", args...)
	os.Exit(1)
}
