package net.consensys.linea.testing;

import com.fasterxml.jackson.databind.JsonNode;
import com.fasterxml.jackson.databind.ObjectMapper;
import java.io.IOException;
import java.io.InputStream;
import java.nio.charset.StandardCharsets;
import org.apache.tuweni.bytes.Bytes;
import org.web3j.tx.Contract;

public class SmartContractUtils {

  private static final ObjectMapper objectMapper = new ObjectMapper();
  private static final ClassLoader classLoader = SmartContractUtils.class.getClassLoader();

  /**
   * The solidity contracts under testing/src/main/solidity are compiled using the solidity plugin
   * and then the web3j plugin generates java wrapper classes for each solidity contract. This
   * method reads the runtime bytecode from the compiler output (.bin-runtime file).
   *
   * @param contractClass: The web3j wrapper class that is autogenerated from the solidity contract
   * @return The deployed or runtime bytecode for the solidity contract
   */
  public static Bytes getSolidityContractRuntimeByteCode(Class<? extends Contract> contractClass) {
    return readSolidityArtifact(contractClass.getSimpleName(), "bin-runtime");
  }

  /**
   * @param contractClass: The web3j wrapper class that is autogenerated from the solidity contract
   * @return The compiled bytecode for the solidity contract
   */
  public static Bytes getSolidityContractCompiledByteCode(Class<? extends Contract> contractClass) {
    return readSolidityArtifact(contractClass.getSimpleName(), "bin");
  }

  private static Bytes readSolidityArtifact(String contractName, String extension) {
    String resourcePath = "solidity/%s.%s".formatted(contractName, extension);
    try (InputStream is = classLoader.getResourceAsStream(resourcePath)) {
      if (is == null) {
        throw new RuntimeException("Could not find contract bytecode resource: " + resourcePath);
      }
      String hex = new String(is.readAllBytes(), StandardCharsets.UTF_8).trim();
      return Bytes.fromHexStringLenient(hex);
    } catch (IOException e) {
      throw new RuntimeException("Could not read contract bytecode: " + resourcePath, e);
    }
  }

  /**
   * The yul contracts under testing/src/main/yul are compiled using the solidity compiler via the
   * yul plugin This method reads the output of the solidity compiler output file and returns the
   * deployed or runtime bytecode for yul contract.
   *
   * @param yulFileName: The yul contract filename along with .yul suffix
   * @return The deployed or runtime bytecode for the yul contract
   */
  public static Bytes getYulContractRuntimeByteCode(final String yulFileName) {
    return readYulArtifact(yulFileName, "deployedBytecode");
  }

  /**
   * @param yulFileName: The yul contract filename along with .yul suffix
   * @return The compiled bytecode for the yul contract
   */
  public static Bytes getYulContractCompiledByteCode(final String yulFileName) {
    return readYulArtifact(yulFileName, "bytecode");
  }

  private static Bytes readYulArtifact(final String yulFileName, final String evmBytecodeKey) {
    if (!yulFileName.contains(".yul")) {
      throw new IllegalArgumentException("Yul file name should contain the suffix: .yul");
    }
    String yulFileNameWithoutSuffix = yulFileName.replace(".yul", "");
    String contractResourcePath = "yul/%s.json".formatted(yulFileNameWithoutSuffix);
    try (InputStream is = classLoader.getResourceAsStream(contractResourcePath)) {
      if (is == null) {
        throw new RuntimeException("Could not find contract resource: " + contractResourcePath);
      }
      JsonNode jsonRoot = objectMapper.readTree(is);
      String byteCode =
          jsonRoot
              .get("contracts")
              .get(yulFileName)
              .get(yulFileNameWithoutSuffix)
              .get("evm")
              .get(evmBytecodeKey)
              .get("object")
              .asText();
      return Bytes.fromHexStringLenient(byteCode);
    } catch (Exception e) {
      throw new RuntimeException("Could not find contract bytecode", e);
    }
  }
}
